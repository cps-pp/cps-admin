import React from 'react';
import {
  iconAddRounded,
  iconEdit,
  iconSearch,
  iconTrash,
  iconView,
} from '@/configs/icon';

export const TableAction = ({
  onEdit,
  onDelete,
  onAdd,
  onSearch,
  onView,
}) => {
  const buttonCount = [onAdd, onSearch, onView, onEdit, onDelete].filter(Boolean).length;
  const gapClass = buttonCount >= 4 ? 'gap-2' : 'gap-4';

  return (
    <div className={`grid w-[60px] grid-cols-3 ${gapClass}`}>
      {onAdd && (
        <button className="w-6 rounded-full" onClick={onAdd}>
          {iconAddRounded}
        </button>
      )}
      {onSearch && <button onClick={onSearch}>{iconSearch}</button>}
      {onView && <button onClick={onView}>{iconView}</button>}
      {onEdit && (
        <button onClick={onEdit} className="text-purple-600 hover:text-purple-800">
          {iconEdit}
        </button>
      )}
      {onDelete && (
        <button onClick={onDelete} className="text-red-600 hover:text-red-800 bg-red-100 rounded-full">
          {iconTrash}
        </button>
      )}
    </div>
  );
};
